    protected void datatable() {
        Object[] Baris = {"Id Pemesanan","Id Pelanggan","Id Paket","Id Venue","Tanggal", "Tamu", "Request", "Status"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = cari.getText();
        try {
            String sql = "SELECT * FROM pemesanan_event WHERE id_pemesanan LIKE '%" + cariitem + "%' "
                    + "OR id_pelanggan LIKE '%" + cariitem + "%' "
                    + "OR id_paket LIKE '%" + cariitem + "%' "
                    + "OR id_venue LIKE '%" + cariitem + "%' "
                    + "OR tanggal_acara LIKE '%" + cariitem + "%' "
                    + "OR jumlah_tamu LIKE '%" + cariitem + "%' "
                    + "OR request_tambahan LIKE '%" + cariitem + "%' "
                    + "OR status_pemesanan LIKE '%" + cariitem + "%' "
                    + "ORDER BY id_pemesanan ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),
                    hasil.getString(7),
                    hasil.getString(8)
                });
            }
            tabelpsn.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil" + e);
            System.out.println("Jumlah data di tabel: " + tabmode.getRowCount());
        }
    }
    
