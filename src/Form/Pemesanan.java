    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String id_pem = idpmsn.getText().trim();
String id_plg = idplgn.getText().trim();
String id_pak = idpkt.getText().trim();
String id_ven = idvn.getText().trim();
String jumlahtm = jmlhtm.getText().trim();
String request = req.getText().trim();
String status = combostatus.getSelectedItem().toString();

// Validasi
if (id_pem.isEmpty() || id_plg.isEmpty() || id_pak.isEmpty() || id_ven.isEmpty() || jumlahtm.isEmpty() || request.isEmpty() || status.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
    return;
}
if (id_pem.length() > 10) {
    JOptionPane.showMessageDialog(this, "ID Pemesanan harus terdiri dari 10 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
    return;
}
if (idSudahTerdaftar(id_pem)) {
    JOptionPane.showMessageDialog(this, "ID Pemesanan sudah terdaftar.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
    return;
}

// Simpan data
String sql = "INSERT INTO pemesanan_event VALUES(?,?,?,?,?,?,?,?)";
try {
    PreparedStatement stat = conn.prepareStatement(sql);
    java.util.Date utilDate = jtgl.getDate();
    java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

    stat.setString(1, id_pem);
    stat.setString(2, id_plg);
    stat.setString(3, id_pak);
    stat.setString(4, id_ven);
    stat.setDate(5, sqlDate);
    stat.setString(6, jumlahtm);
    stat.setString(7, request);
    stat.setString(8, status);

    stat.executeUpdate();
    JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
    kosong();
    aktif();

    // Kosongkan pencarian
    cari.setText("");
    datatable();
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Data gagal disimpan! " + e);
}

    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        String id_pem = idpmsn.getText().trim(); // Trim untuk menghapus spasi di awal dan akhir
        String id_plg = idplgn.getText().trim();
        String id_pak = idpkt.getText().trim();
        String id_ven = idvn.getText().trim();
        String jumlahtm = jmlhtm.getText().trim();
        String request = req.getText().trim();
        String status = combostatus.getSelectedItem().toString();
        if (id_pem.isEmpty() || id_plg.isEmpty() || id_pak.isEmpty() || id_ven.isEmpty() || jumlahtm.isEmpty() || request.isEmpty() || status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (id_pem.length() > 10) {
            JOptionPane.showMessageDialog(this, "ID Pelanggan harus terdiri dari 10 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "UPDATE pemesanan_event set id_pelanggan=?,id_paket=?,id_venue=?,tanggal_acara=?,jumlah_tamu=?,request_tambahan=?,status_pemesanan=? where id_pemesanan=?" ;
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            
            java.util.Date utilDate = jtgl.getDate();
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
            
            stat.setString(1, id_plg);
            stat.setString(2, id_pak);
            stat.setString(3, id_ven);
            stat.setDate(4, sqlDate);
            stat.setString(5, jumlahtm);
            stat.setString(6, request);
            stat.setString(7, status);
            stat.setString(8, id_pem);

            int rowsUpdated = stat.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                kosong();
                aktif();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal diubah. ID Pemesanan tidak ditemukan.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan!" + e);
        }
        datatable();
    }//GEN-LAST:event_bubahActionPerformed

