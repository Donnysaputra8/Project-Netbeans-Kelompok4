    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String idtxt = id_paket.getText().trim(); // Trim untuk menghapus spasi di awal dan akhir
        String nptxt = nama_paket.getText().trim();
        String destxt = deskripsi.getText().trim();
        String hartxt = harga.getText().trim();
        if (idtxt.isEmpty() || nptxt.isEmpty() || destxt.isEmpty() || hartxt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (idtxt.length() > 10) {
            JOptionPane.showMessageDialog(this, "ID Paket harus terdiri dari 10 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (idSudahTerdaftar(idtxt)) {
            JOptionPane.showMessageDialog(this, "ID Paket sudah terdaftar.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "insert into paket_event values(?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, idtxt);
            stat.setString(2, nptxt);
            stat.setString(3, destxt);
            stat.setString(4, hartxt);

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            kosong();
            aktif();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan!" + e);
        }
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        String idtxt = id_paket.getText().trim(); // Trim untuk menghapus spasi di awal dan akhir
        String nptxt = nama_paket.getText().trim();
        String destxt = deskripsi.getText().trim();
        String hartxt = harga.getText().trim();
        if (idtxt.isEmpty() || nptxt.isEmpty() || destxt.isEmpty() || hartxt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (idtxt.length() > 10) {
            JOptionPane.showMessageDialog(this, "ID Paket harus terdiri dari 10 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "UPDATE paket_event set nama_paket=?,deskripsi=?,harga=? where id_paket=?" ;
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nptxt);
            stat.setString(2, destxt);
            stat.setString(3, hartxt);
            stat.setString(4, idtxt);

            int rowsUpdated = stat.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                kosong();
                aktif();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal diubah. ID Paket tidak ditemukan.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan!" + e);
        }
        datatable();
    }//GEN-LAST:event_bubahActionPerformed

