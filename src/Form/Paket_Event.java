    protected void datatable() {
        Object[] Baris = {"Id Paket", "Nama Paket", "Deskripsi", "Harga"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = cari.getText();
        try {
            String sql = "SELECT * FROM paket_event WHERE id_paket LIKE '%" + cariitem + "%' "
                    + "OR nama_paket LIKE '%" + cariitem + "%' "
                    + "OR deskripsi LIKE '%" + cariitem + "%' "
                    + "OR harga LIKE '%" + cariitem + "%' "
                    + "ORDER BY id_paket ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                });
            }
            tpaket.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil" + e);
        }
    }

