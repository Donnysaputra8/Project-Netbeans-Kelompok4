    protected void datatable() {
        Object[] Baris = {"Id Venue", "Nama", "Alamat", "Kapasitas", "Harga Sewa"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = cari.getText();
        try {
            String sql = "SELECT * FROM venue WHERE id_venue LIKE '%" + cariitem + "%' "
                    + "OR nama_venue LIKE '%" + cariitem + "%' "
                    + "OR alamat_venue LIKE '%" + cariitem + "%' "
                    + "OR kapasitas LIKE '%" + cariitem + "%' "
                    + "OR harga_sewa LIKE '%" + cariitem + "%' "
                    + "ORDER BY id_venue ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                });
            }
            tvenue.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil" + e);
        }
    }

