    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String txt_id = id_admin.getText().trim(); // Trim untuk menghapus spasi di awal dan akhir
        String txt_username = username.getText().trim();
        String txt_password = password.getText().trim();
        String txt_nama = nama.getText().trim();
        if (txt_id.isEmpty() || txt_username.isEmpty() || txt_password.isEmpty() || txt_nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txt_id.length() > 10) {
            JOptionPane.showMessageDialog(this, "ID Admin harus terdiri dari 10 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (idSudahTerdaftar(txt_id)) {
            JOptionPane.showMessageDialog(this, "ID Admin sudah terdaftar.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "insert into user_admin values(?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txt_id);
            stat.setString(2, txt_username);
            stat.setString(3, txt_password);
            stat.setString(4, txt_nama);

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            kosong();
            aktif();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan!" + e);
        }
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        String txt_id = id_admin.getText().trim(); // Trim untuk menghapus spasi di awal dan akhir
        String txt_username = username.getText().trim();
        String txt_password = password.getText().trim();
        String txt_nama = nama.getText().trim();
        if (txt_id.isEmpty() || txt_username.isEmpty() || txt_password.isEmpty() || txt_nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txt_id.length() > 10) {
            JOptionPane.showMessageDialog(this, "ID Admin harus terdiri dari 10 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "UPDATE user_admin set username=?,password=?,nama=? where id_admin=?" ;
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txt_username);
            stat.setString(2, txt_password);
            stat.setString(3, txt_nama);
            stat.setString(4, txt_id);

            int rowsUpdated = stat.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                kosong();
                aktif();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal diubah. ID Admin tidak ditemukan.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan!" + e);
        }
    datatable();
    }//GEN-LAST:event_bubahActionPerformed

